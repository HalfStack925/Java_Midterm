
public class Person  {

    String fName;
    String mName;
    String middlel;
    String lname;
    String DOB;
    String SSN;
    String phone;
    String address;
    String allergies;
    String type;
    String courses;


    public Person(String fName, String mName, String middlel, String lname, String DOB, String SSN, String phone,
                  String address, String allergies, String type, String courses) {
        this.fName = fName;
        this.mName = mName;
        this.middlel = middlel;
        this.lname = lname;
        this.DOB = DOB;
        this.SSN = SSN;
        this.phone = phone;
        this.address = address;
        this.allergies = allergies;
        this.type = type;
        this.courses = courses;
    }

}
